def video_info(path):
    cap = cv2.VideoCapture(str(path))
    if not cap.isOpened():
        return {"exists": Path(path).exists(), "readable": False}
    frames = int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    width = int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    height = int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    fps = float(cap.get(cv2.CAP_PROP_FPS))
    cap.release()
    return {
        "exists": True,
        "readable": True,
        "frames": frames,
        "width": width,
        "height": height,
        "fps": fps,
    }


def count_images(path, exts=("*.jpg", "*.png", "*.jpeg")):
    p = Path(path)
    if not p.exists():
        return 0
    total = 0
    for ext in exts:
        total += len(list(p.glob(ext)))
    return total


def read_split(path):
    p = Path(path)
    if not p.exists():
        return []
    return [line.strip() for line in p.read_text(encoding="utf-8").splitlines() if line.strip()]


def inspect_davis_root(root):
    root = Path(root)
    train_txt = root / "ImageSets/2017/train.txt"
    val_txt = root / "ImageSets/2017/val.txt"
    train_names = read_split(train_txt)
    val_names = read_split(val_txt)
    image_roots = [
        root / "JPEGImages/Full-Resolution",
        root / "JPEGImages/480p",
    ]
    mask_roots = [
        root / "Annotations_unsupervised/Full-Resolution",
        root / "Annotations_unsupervised/480p",
        root / "Annotations/Full-Resolution",
        root / "Annotations/480p",
    ]
    depth_roots = [
        root / "depthImages/Full-Resolution",
        root / "depthImages/480p",
    ]
    best_image_root = next((p for p in image_roots if p.exists()), None)
    best_mask_root = next((p for p in mask_roots if p.exists()), None)
    best_depth_root = next((p for p in depth_roots if p.exists()), None)
    seqs = sorted([p.name for p in best_image_root.iterdir() if p.is_dir()]) if best_image_root else []
    frame_count = sum(count_images(best_image_root / seq, ("*.jpg",)) for seq in seqs) if best_image_root else 0
    depth_count = 0
    if best_depth_root:
        depth_count = sum(count_images(best_depth_root / seq, ("*.png",)) for seq in seqs)
    return {
        "dataset": "DAVIS",
        "root": str(root),
        "exists": root.exists(),
        "streamsplatable": bool(train_names or val_names) and best_image_root is not None,
        "train_sequences": len(train_names),
        "val_sequences": len(val_names),
        "image_root": str(best_image_root) if best_image_root else "",
        "mask_root": str(best_mask_root) if best_mask_root else "",
        "depth_root": str(best_depth_root) if best_depth_root else "",
        "sequence_count_detected": len(seqs),
        "frame_count_detected": frame_count,
        "depth_count_detected": depth_count,
        "notes": "Expected by provider_davis: ImageSets/2017/*.txt, JPEGImages/Full-Resolution, Annotations_unsupervised/Full-Resolution, depthImages generated by preprocessing.",
    }


def inspect_vos_root(root):
    root = Path(root)
    train_images = root / "train/JPEGImages"
    valid_images = root / "valid/JPEGImages"
    train_seqs = sorted([p.name for p in train_images.iterdir() if p.is_dir()]) if train_images.exists() else []
    valid_seqs = sorted([p.name for p in valid_images.iterdir() if p.is_dir()]) if valid_images.exists() else []
    train_frames = sum(count_images(train_images / seq, ("*.jpg",)) for seq in train_seqs) if train_images.exists() else 0
    valid_frames = sum(count_images(valid_images / seq, ("*.jpg",)) for seq in valid_seqs) if valid_images.exists() else 0
    return {
        "dataset": "YouTube-VOS",
        "root": str(root),
        "exists": root.exists(),
        "streamsplatable": train_images.exists() or valid_images.exists(),
        "train_sequences": len(train_seqs),
        "val_sequences": len(valid_seqs),
        "image_root": str(train_images) if train_images.exists() else str(valid_images) if valid_images.exists() else "",
        "mask_root": str(root / "train/Annotations") if (root / "train/Annotations").exists() else "",
        "depth_root": str(root / "train/depthImages") if (root / "train/depthImages").exists() else "",
        "sequence_count_detected": len(train_seqs) + len(valid_seqs),
        "frame_count_detected": train_frames + valid_frames,
        "depth_count_detected": 0,
        "notes": "Expected by provider_vos: train/valid JPEGImages and train Annotations; depthImages generated by preprocessing.",
    }


def inspect_generic_root(dataset, root):
    root = Path(root)
    return {
        "dataset": dataset,
        "root": str(root),
        "exists": root.exists(),
        "streamsplatable": root.exists(),
        "train_sequences": "",
        "val_sequences": "",
        "image_root": "",
        "mask_root": "",
        "depth_root": "",
        "sequence_count_detected": "",
        "frame_count_detected": "",
        "depth_count_detected": "",
        "notes": "Candidate root for StreamSplat static training data; detailed inventory requires dataset-specific parser.",
    }


def inspect_current_samples():
    rows = []
    for name, path in CURRENT_SAMPLES.items():
        info = video_info(path)
        rows.append({
            "dataset": "current_sample",
            "sample": name,
            "path": path,
            **info,
        })
    return rows


def write_csv(rows, path):
    fields = [
        "dataset", "root", "exists", "streamsplatable", "train_sequences", "val_sequences",
        "image_root", "mask_root", "depth_root", "sequence_count_detected",
        "frame_count_detected", "depth_count_detected", "notes",
    ]
    with open(path, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def write_current_csv(rows, path):
    fields = ["dataset", "sample", "path", "exists", "readable", "frames", "width", "height", "fps"]
    with open(path, "w", encoding="utf-8", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=fields)
        writer.writeheader()
        writer.writerows(rows)


def parse_args():
    parser = argparse.ArgumentParser()
    parser.add_argument("--summary_root", type=Path, default=DEFAULT_SUMMARY_ROOT)
    parser.add_argument("--davis_roots", nargs="*", default=DEFAULT_DAVIS_CANDIDATES)
    parser.add_argument("--vos_roots", nargs="*", default=DEFAULT_VOS_CANDIDATES)
    parser.add_argument("--re10k_roots", nargs="*", default=DEFAULT_RE10K_CANDIDATES)
    parser.add_argument("--co3d_roots", nargs="*", default=DEFAULT_CO3D_CANDIDATES)
    return parser.parse_args()


def main():
    args = parse_args()
    args.summary_root.mkdir(parents=True, exist_ok=True)
    dataset_rows = []
    dataset_rows.extend(inspect_davis_root(root) for root in args.davis_roots)
    dataset_rows.extend(inspect_vos_root(root) for root in args.vos_roots)
    dataset_rows.extend(inspect_generic_root("RE10K", root) for root in args.re10k_roots)
    dataset_rows.extend(inspect_generic_root("CO3D", root) for root in args.co3d_roots)
    current_rows = inspect_current_samples()

    available = [row for row in dataset_rows if row["streamsplatable"]]
    missing = [row for row in dataset_rows if not row["streamsplatable"]]
    summary = {
        "current_samples": current_rows,
        "dataset_candidates": dataset_rows,
        "available_candidates": available,
        "missing_candidates": missing,
        "recommendation": (
            "Current four sample videos are available. No full DAVIS/YouTube-VOS root was detected in default candidate paths; "
            "download or mount DAVIS 2017 and run depth preprocessing before Stage 8 expansion."
        ),
    }
    summary_path = args.summary_root / "stage7_dataset_inventory_summary.json"
    dataset_csv = args.summary_root / "stage7_dataset_inventory_candidates.csv"
    current_csv = args.summary_root / "stage7_current_samples.csv"
    summary_path.write_text(json.dumps(summary, indent=2), encoding="utf-8")
    write_csv(dataset_rows, dataset_csv)
    write_current_csv(current_rows, current_csv)
    print(json.dumps({
        "summary": str(summary_path),
        "dataset_csv": str(dataset_csv),
        "current_csv": str(current_csv),
        "available_candidates": len(available),
        "missing_candidates": len(missing),
    }, indent=2))


if __name__ == "__main__":
    raise SystemExit(main())
